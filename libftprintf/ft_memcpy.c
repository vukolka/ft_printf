#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		((char*)dest)[i] = *(char*)src;
		src = (char*)src + 1;
		i++;
	}
	return (dest);
}
