#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	while (n > 0 && *S1 == *S2)
	{
		s1 = S1 + 1;
		s2 = S2 + 1;
		n--;
	}
	if (!n)
		return (0);
	return (*S1 - *S2);
}
