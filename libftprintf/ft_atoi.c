#include "libft.h"

int		ft_atoi(const char *str)
{
	int res;
	int a;

	a = 1;
	res = 0;
	while (*str == '\t' || *str == '\n' || *str == '\r' ||
		*str == '\v' || *str == '\f' || *str == ' ')
		str++;
	if (*str == '-')
	{
		a = -1;
		str++;
	}
	if (*str == '+' && *(str - 1) != '-')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (res * a);
}
